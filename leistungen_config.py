def stadt_zu_slug(stadt):
    """Konvertiert Stadtname zu URL-freundlichem Slug"""
    return stadt.lower().replace('ä', 'ae').replace('ö', 'oe').replace('ü', 'ue').replace('ß', 'ss').replace(' ', '-')

def slug_zu_stadt(slug):
    """Konvertiert Slug zurück zu Stadtname"""
    for stadt in SEO_STAEDTE:
        if stadt_zu_slug(stadt) == slug:
            return stadt
    return slug.replace('-', ' ').title()

def get_leistung_seo(leistung_slug, stadt):
    """Gibt SEO-Daten für eine Leistung + Stadt Kombination zurück"""
    leistung = LEISTUNGEN.get(leistung_slug)
    if not leistung:
        return None
    
    return {
        'name': leistung['name'],
        'h1': leistung['h1'].format(stadt=stadt),
        'intro': leistung['intro'].format(stadt=stadt),
        'h2': leistung['h2'].format(stadt=stadt),
        'footer': leistung['footer'].format(stadt=stadt),
        'meta_title': leistung['meta_title'].format(stadt=stadt),
        'meta_description': leistung['meta_description'].format(stadt=stadt)
    }
